package Day013.test;

public class phoneNumber {
	private String name;
	private String pn;
	
	public phoneNumber(String name, String pn) {
		this.name = name;
		this.pn = pn;
	}

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPn() {
		return pn;
	}
	public void setPn(String pn) {
		this.pn = pn;
	}
}
