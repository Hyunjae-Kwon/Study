package Day007.Chap06.Example.ex16;

public class Printer {
	//작성 위치
	void println(int value){
		System.out.println(value);
	}
	void println(boolean value) {
		System.out.println(value);
	}
	void println(double value) {
		System.out.println(value);
	}
	void println(String value) {
		System.out.println(value);
	}
}
